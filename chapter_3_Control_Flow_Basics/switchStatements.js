//switch statements
const grade = "D";

switch (grade) {
  case "A":
    console.log("Your Grade A:", grade);
    break;
  case "B":
    console.log("Your Grade B:", grade);
    break;
  case "C":
    console.log("Your Grade C:", grade);
    break;
  case "D":
    console.log("Your Grade D:", grade);
    break;
  default:
    console.log("Not a valid grade");
    break;
}
